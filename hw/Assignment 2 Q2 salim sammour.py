num1 = int(input("enter first value"))
num2 = int(input("enter second value"))


# # 1.Return the addition of the numbers
def add(num1, num2):
    return num1 + num2


print("addition= ")
print(add(num1, num2))


# # 2.Return the subtraction of the numbers
def sub(num1, num2):
    return num1 - num2


print("subtraction= ")
print(sub(num1, num2))


# # 3.Return the multiplication of the numbers
def mult(num1, num2):
    return num1 * num2


print("multiplication= ")
print(mult(num1, num2))


# # 4.Return the normal division of the numbers
def ndiv(num1, num2):
    return num1 / num2


print(" normal division= ")
print(ndiv(num1, num2))


# # 5.Return the integer division of the numbers
def idiv(num1, num2):
    return int(num1 / num2)


print("integer division= ")
print(idiv(num1, num2))


# # 6.Return the exponential of the numbers , suppose a number to be the power.
def exp(num1, num2):
    return num1 ** num2


print("exponential= ")
print(exp(num1, num2))

